#include <stdio.h>
int main(void)
{
    int i = 0, j = 0;
    scanf("%d %d", &i, &j);
    printf("%d + %d = %d\n", i, j, i + j);
    printf("%d - %d = %d\n", i, j, i - j);
    printf("%d * %d = %d\n", i, j, i * j);
    printf("%d / %d = %d\n", i, j, i / j);
    printf("%d %% %d = %d\n", i, j, i % j);
    
    
    
}
